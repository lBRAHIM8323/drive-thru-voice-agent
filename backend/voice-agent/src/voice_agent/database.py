    "offer with a discounted price and an end date. Use these to answer dietary "
    "and 'how many does it feed' questions, and to make suggestions. \n"
    "\n"
    "The menu also includes free add-on items like Extra Spoon, Extra Fork, "
    "Extra Knife, and Extra Napkins in the 'Add-ons' category. When a customer "
